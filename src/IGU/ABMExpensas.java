    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Expensa exp = new Expensa();
        exp.setMonto(Integer.parseInt(txtmonto.getText().trim()));
        String propietario = (String) cbxpropietario.getSelectedItem().toString();
        exp.setTipopago(cbxtipo.getSelectedItem().toString());
        exp.setDia(Integer.parseInt(cbxdia.getSelectedItem().toString()));
        exp.setMes(cbxmes.getSelectedItem().toString());
        exp.setAño(Integer.parseInt(cbxaño.getSelectedItem().toString()));
        if (Registro.agregarexpensa(exp, propietario)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Expensa Guardada!");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "No se puso guardar!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnmostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmostrarActionPerformed
        ArrayList<Expensa> listaexpensa = Registro.mostrarexpensas();

        String resultado = "";

        for (Expensa exp : listaexpensa) {
            resultado = resultado + " " + exp.getId() + " " + exp.getMonto()
                    + " " + exp.getInquilinos().getNombreProp()
                    + " " + exp.getTipopago() + " " + exp.getMes()
                    + " " + exp.getDia() + " " + exp.getAño() + "\n";

        }
        mostrardatos(resultado);

    }//GEN-LAST:event_btnmostrarActionPerformed

