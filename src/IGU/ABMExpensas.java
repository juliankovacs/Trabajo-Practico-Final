    void mostrardatos(String valor) {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID Exp");
        modelo.addColumn("ID Prop");
        modelo.addColumn("Monto");
        modelo.addColumn("Tipo");
        modelo.addColumn("Mes");
        modelo.addColumn("Dia");
        modelo.addColumn("Año");
        txtsalida.setModel(modelo);
        String sql = "";
        if (valor.equals("")) {
            sql = "SELECT * FROM expensas";
        } else {
            sql = "SELECT * FROM expensas WHERE ID_Expensa='" + valor + "'";
        }

        String[] datos = new String[7];
        try {
            Conexion cx = new Conexion();
            Connection c = cx.conexion();
            Statement st = c.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                datos[5] = rs.getString(6);
                datos[6] = rs.getString(7);

                modelo.addRow(datos);
            }
            txtsalida.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(ABMExpensas.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    public void llenarcbxpropietario() {
        try {
            Conexion cc = new Conexion();
            Connection cn = cc.conexion();
            String sql = "SELECT nombre FROM propietario";
            PreparedStatement pst = cn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery(sql);
            DefaultComboBoxModel<String> model = (DefaultComboBoxModel<String>) cbxpropietario.getModel();
            while (rs.next()) {
                String nombre = rs.getString("nombre");
                model.addElement(nombre);
            }
        } catch (SQLException s) {
            System.out.print("ERROR DE SQL" + s.getMessage());

        } catch (Exception e) {
            System.out.print("ERROR AL AGREGAR USUARIO" + e.getMessage());
        }
    }

