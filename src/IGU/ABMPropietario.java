        void mostrardatos(String valor) {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID Prop");
        modelo.addColumn("ID Edif");
        modelo.addColumn("Nombre");
        modelo.addColumn("Piso");
        modelo.addColumn("Dpto");
        modelo.addColumn("Telefono");
        String sql = "";
        if (valor.equals("")) {
            sql = "SELECT * FROM propietario";
        } else {
            sql = "SELECT * FROM propietario WHERE ID_Propietario='" + valor + "'";
        }

        String[] datos = new String[7];
        try {
            Conexion cx = new Conexion();
            Connection c = cx.conexion();
            Statement st = c.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                datos[5] = rs.getString(6);

                modelo.addRow(datos);
            }
            txtsalida.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(ABMPropietario.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
        
    public void llenarcbx() {
        try {
            Conexion cc = new Conexion();
            Connection cn = cc.conexion();
            String sql = "SELECT nombreedificio FROM consorcio";
            PreparedStatement pst = cn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery(sql);
            DefaultComboBoxModel<String> model = (DefaultComboBoxModel<String>) cbxEdificio.getModel();
            while (rs.next()) {
                String nombre = rs.getString("nombreedificio");
                model.addElement(nombre);
            }
        } catch (SQLException s) {
            System.out.print("ERROR DE SQL" + s.getMessage());

        } catch (Exception e) {
            System.out.print("ERROR AL AGREGAR USUARIO" + e.getMessage());
        }
    }

