    private void btncrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncrearActionPerformed
        Propietario prop = new Propietario();
        prop.setNombreProp(this.txtnombre.getText().trim());
        String edificio = (String) cbxEdificio.getSelectedItem().toString();
        prop.setPiso(Integer.parseInt(txtpiso.getText().trim()));
        prop.setDepto(this.txtdpto.getText().trim());
        prop.setTelefono(Integer.parseInt(txttelefono.getText().trim()));
        if (Registro.agregarpropietario(prop, edificio)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Propietario Guardado!");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "No se puso guardar!");
        }
    }//GEN-LAST:event_btncrearActionPerformed

    private void btnmostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmostrarActionPerformed

        ArrayList<Propietario> listapropietario = Registro.mostrarTodos();
        
        String resultado = "";
        
        for (Propietario prop : listapropietario) {
            resultado = resultado + " " + prop.getNombreProp()
                    + " " + prop.getConsorcio().getNombreEdificio()
                    + " " + prop.getPiso() + " " + prop.getDepto()
                    + " " + prop.getTelefono()+"\n"; 
           
        }
        mostrardatos(resultado);


    }//GEN-LAST:event_btnmostrarActionPerformed

